#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int	i;
	int	n;

	n = '\n';
	if (!s)
		return ;
	i = -1;
	while (s[++i])
		write(fd, &s[i], 1);
	write(fd, &n, 1);
}
